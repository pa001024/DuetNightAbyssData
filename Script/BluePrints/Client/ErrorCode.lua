local temp = {}
for key, value in pairs(ErrorCode) do
  if temp[value] then
  end
  temp[value] = key
end
ErrorCode.Translate = temp

function ErrorCode:Name(errCode)
  return ErrorCode.Translate[errCode]
end

function ErrorCode:Check(Code, TipsType)
  if Code ~= self.RET_SUCCESS then
    print(_G.LogTag, "ErrorCode Check Result not Success", Code)
    if not GWorld.GameInstance then
      return
    end
    local UIManger = GWorld.GameInstance:GetGameUIManager()
    UIManger:ShowError(Code, nil, TipsType)
    return false
  end
  return true
end

function ErrorCode:GetText(ErrCode)
  local Err = DataMgr.ErrorCode[ErrCode]
  if not Err then
    return
  end
  local Key = CommonConst.SystemLanguage
  if CommonConst.SystemLanguage == CommonConst.SystemLanguages.CN then
    Key = "ErrorCodeContent"
  end
  return Err[Key]
end

return ErrorCode
